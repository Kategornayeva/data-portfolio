        d.industry_domain_name   AS d_industry_domain_name,
        d.author_src_id          AS d_author_src_id,
        d.author_name            AS d_author_name
    FROM src s
LEFT JOIN bl_dm.dim_ideas d
  ON d.idea_src_id   = s.idea_src_id
 AND d.source_system = s.source_system
 AND d.source_entity = s.source_entity
)

