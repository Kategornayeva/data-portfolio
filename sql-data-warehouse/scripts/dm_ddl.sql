        SELECT
            idea_id, idea_title, idea_version,
            category_id, category,
            subcategory_id, subcategory,
            industry_domain_id, industry_domain,
            author_id, author_name,
            source_system,
            'ext_b2c'::varchar(50) AS source_entity
