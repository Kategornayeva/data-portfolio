--======================================================================
-- 2. NO DUPLICATE ACTIVE ROWS PER BUSINESS KEY
--======================================================================
/* Expected: problem_rows = 0 */
SELECT
    'CHK_02_DUPLICATE_ACTIVE' AS check_name,
    COUNT(*)                  AS problem_rows
FROM (
    SELECT customer_src_id, customer_source
    FROM bl_dm.dim_customers_scd
    WHERE is_active = 'Y'
    GROUP BY customer_src_id, customer_source
    HAVING COUNT(*) > 1
) t;

--======================================================================
-- 3. ACTIVE FLAG CONSISTENCY WITH END_DT
--======================================================================
/* Expected: problem_rows = 0
   Rule:
     - Active row must have end_dt = 9999-12-31
     - Inactive row must have end_dt <> 9999-12-31
*/
SELECT
    'CHK_03_ACTIVE_ENDDT_CONSISTENCY' AS check_name,
    COUNT(*)                          AS problem_rows
FROM bl_dm.dim_customers_scd
WHERE (is_active = 'Y' AND end_dt <> DATE '9999-12-31')
   OR (is_active = 'N' AND end_dt =  DATE '9999-12-31');

--======================================================================
-- 4. DATE RANGE VALIDITY
--======================================================================
/* Expected: problem_rows = 0
   Rule: start_dt must be <= end_dt
*/
SELECT
    'CHK_04_DATE_RANGE_VALID' AS check_name,
    COUNT(*)                  AS problem_rows
FROM bl_dm.dim_customers_scd
WHERE start_dt > end_dt;

--======================================================================
-- 5. NO OVERLAPPING PERIODS PER BUSINESS KEY
--======================================================================
/* Expected: problem_rows = 0
   Rule: for the same business key, validity periods must not overlap
*/
SELECT
    'CHK_05_NO_OVERLAPS' AS check_name,
    COUNT(*)             AS problem_rows
FROM (
    SELECT 1
    FROM bl_dm.dim_customers_scd a
    JOIN bl_dm.dim_customers_scd b
      ON a.customer_src_id = b.customer_src_id
     AND a.customer_source = b.customer_source
     AND a.customer_surr_id < b.customer_surr_id
    WHERE a.start_dt <= b.end_dt
      AND b.start_dt <= a.end_dt
) t;

