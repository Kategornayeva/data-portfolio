/*======================================================================
  Layer  : BL_DM
  Object : DIM_CUSTOMERS_SCD
  Type   : DDL + DML
  SCD    : Type 2
  DB     : PostgreSQL
  Notes  :
    - Rerunnable / reusable (IF NOT EXISTS, NOT EXISTS)
    - Default row included
    - NULL handling via COALESCE
    - DATE fields only
    - Change detection uses IS DISTINCT FROM (NULL-safe)
    - PK generated by SEQUENCE via DEFAULT (no nextval in INSERT)
    - LEFT JOIN approach for compare
    - COMMIT after DML blocks (BEGIN/COMMIT to avoid "no transaction" warning)
  Sources:
    - sa_b2c.ext_b2c columns used: customer_id, customer_name, customer_email, device_type, payment_method, source_system
    - sa_b2b.ext_b2b columns used: customer_id, company_name, company_industry, tax_id, source_system
======================================================================*/

