        idea_category_name,
        idea_subcategory_id,
        idea_subcategory_name,
        industry_domain_src_id,
        industry_domain_name,
        author_src_id,
        author_name,
        source_system,
        MIN(source_entity) AS source_entity
    FROM src_raw
    GROUP BY
        idea_src_id,
        idea_title,
        idea_version,
        idea_category_id,
        idea_category_name,
        idea_subcategory_id,
        idea_subcategory_name,
        industry_domain_src_id,
        industry_domain_name,
        author_src_id,
