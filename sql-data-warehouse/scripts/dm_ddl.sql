            subcategory_id, subcategory,
            industry_domain_id, industry_domain,
            author_id, author_name,
            source_system,
            'ext_b2b'::varchar(50) AS source_entity
        FROM sa_b2b.ext_b2b
        WHERE idea_id IS NOT NULL
