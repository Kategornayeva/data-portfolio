--======================================================================
-- 4.1 CLOSE OLD VERSION (changed active rows)
--======================================================================
BEGIN;

WITH src AS (
    /* -------- B2C customers -------- */
    SELECT
        CAST(c.customer_id AS VARCHAR(100))             AS customer_src_id,
        'B2C'                                           AS customer_source,

        MAX(COALESCE(c.customer_name,  'n.a.'))         AS customer_name,
        MAX(COALESCE(c.customer_email, 'n.a.'))         AS customer_email,
        MAX(COALESCE(c.device_type,    'n.a.'))         AS device_type,
        MAX(COALESCE(c.payment_method, 'n.a.'))         AS payment_method,

        'n.a.'                                          AS company_name,
        'n.a.'                                          AS company_industry,
        'n.a.'                                          AS tax_id,

        MAX(COALESCE(c.source_system, 'n.a.'))          AS source_system,
        'ext_b2c'                                       AS source_entity
    FROM sa_b2c.ext_b2c c
    WHERE c.customer_id IS NOT NULL
    GROUP BY c.customer_id

    UNION ALL

    /* -------- B2B customers -------- */
    SELECT
        CAST(b.customer_id AS VARCHAR(100))             AS customer_src_id,
        'B2B'                                           AS customer_source,

        'n.a.'                                          AS customer_name,
        'n.a.'                                          AS customer_email,
        'n.a.'                                          AS device_type,
        'n.a.'                                          AS payment_method,

        MAX(COALESCE(b.company_name,     'n.a.'))       AS company_name,
        MAX(COALESCE(b.company_industry, 'n.a.'))       AS company_industry,
        MAX(COALESCE(b.tax_id,           'n.a.'))       AS tax_id,

        MAX(COALESCE(b.source_system, 'n.a.'))          AS source_system,
        'ext_b2b'                                       AS source_entity
    FROM sa_b2b.ext_b2b b
    WHERE b.customer_id IS NOT NULL
    GROUP BY b.customer_id
),
cmp AS (
    /* Compare incoming source with current active version in DIM */
    SELECT
        s.*,
        d.customer_surr_id                               AS active_customer_surr_id,

        d.customer_name                                  AS d_customer_name,
        d.customer_email                                 AS d_customer_email,
        d.device_type                                    AS d_device_type,
        d.payment_method                                 AS d_payment_method,
        d.company_name                                   AS d_company_name,
        d.company_industry                               AS d_company_industry,
        d.tax_id                                         AS d_tax_id
    FROM src s
    LEFT JOIN bl_dm.dim_customers_scd d
      ON d.customer_src_id = s.customer_src_id
     AND d.customer_source = s.customer_source
     AND d.is_active = 'Y'
),
chg AS (
    /* Identify only changed records among active ones */
    SELECT c.active_customer_surr_id AS customer_surr_id
    FROM cmp c
    WHERE c.active_customer_surr_id IS NOT NULL
      AND (
           c.customer_name     IS DISTINCT FROM c.d_customer_name
        OR c.customer_email    IS DISTINCT FROM c.d_customer_email
        OR c.device_type       IS DISTINCT FROM c.d_device_type
        OR c.payment_method    IS DISTINCT FROM c.d_payment_method
        OR c.company_name      IS DISTINCT FROM c.d_company_name
        OR c.company_industry  IS DISTINCT FROM c.d_company_industry
        OR c.tax_id            IS DISTINCT FROM c.d_tax_id
      )
)
UPDATE bl_dm.dim_customers_scd d
SET
    end_dt    = CURRENT_DATE - 1,
    is_active = 'N'
WHERE d.customer_surr_id IN (SELECT customer_surr_id FROM chg)
  AND d.is_active = 'Y'
  AND d.end_dt = DATE '9999-12-31';

COMMIT;

